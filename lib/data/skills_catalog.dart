/// 模糊搜尋（不分大小寫，支援部分字串）
List<String> searchSkills(String query, {Set<String> excluded = const {}}) {
  final q = query.trim().toLowerCase();
  return skillsCatalog
      .where((s) => !excluded.contains(s))
      .where((s) => q.isEmpty || s.toLowerCase().contains(q))
      .toList();
}
